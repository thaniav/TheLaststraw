class ProductModel{
  String name;
  int price;
  int quantity;
  int oldprice;
  String picture;
  String details;
  ProductModel(String name,int price, int quantity,int oldprice,String picture,String details)
  {
    this.name=name;
    this.price=price;
    this.quantity=quantity;
    this.oldprice=oldprice;
    this.picture=picture;
    this.details=details;
  }

}